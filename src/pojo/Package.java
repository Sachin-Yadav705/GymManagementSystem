package pojo;

public class Package {


	private int packid;
	private String packname;
	private String packcategory;
	private int packfee;
	private int packduration;
	private String packdescription;
	
	public int getPackid() {
		return packid;
	}
	public void setPackid(int packid) {
		this.packid = packid;
	}
	public String getPackname() {
		return packname;
	}
	public void setPackname(String packname) {
		this.packname = packname;
	}
	public String getPackcategory() {
		return packcategory;
	}
	public void setPackcategory(String packcategory) {
		this.packcategory = packcategory;
	}
	public int getPackfee() {
		return packfee;
	}
	public void setPackfee(int packfee) {
		this.packfee = packfee;
	}
	public int getPackduration() {
		return packduration;
	}
	public void setPackduration(int packduration) {
		this.packduration = packduration;
	}
	public String getPackdescription() {
		return packdescription;
	}
	public void setPackdescription(String packdescription) {
		this.packdescription = packdescription;
	}
	
	
}
